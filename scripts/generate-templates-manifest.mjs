#!/usr/bin/env node
// Codegen — CLI-031.
// Scanne packages/arc-cli/src/templates/__templates__/*.eta et écrit
// un manifest TypeScript qui expose un Record<basename, content>
// consommé par packages/arc-cli/src/templates/render.ts.
//
// Output : packages/arc-cli/src/templates-manifest.ts (gitignored).
// Re-exécuté à chaque pnpm prebuild / pretest / pretypecheck pour
// garantir que le manifest reflète les .eta sources.
//
// Pattern cloné de DIST-001 1a-2 (playbooks-manifest). Strings inline
// JSON.stringify, embarquées dans le binaire par `bun build --compile`.

import { mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");
const templatesDir = resolve(repoRoot, "packages/arc-cli/src/templates/__templates__");
const outFile = resolve(repoRoot, "packages/arc-cli/src/templates-manifest.ts");

const files = readdirSync(templatesDir, { withFileTypes: true })
  .filter((entry) => entry.isFile() && entry.name.endsWith(".eta"))
  .map((entry) => entry.name)
  .sort();

const entries = files.map((name) => {
  const content = readFileSync(resolve(templatesDir, name), "utf-8");
  return { name, content };
});

const banner = `// AUTO-GENERATED by scripts/generate-templates-manifest.mjs.
// DO NOT EDIT. Regenerated by pnpm prebuild / pretest / pretypecheck.
// Source : packages/arc-cli/src/templates/__templates__/ (${entries.length} files).`;

const entryLines = entries.map(
  ({ name, content }) => `  ${JSON.stringify(name)}: ${JSON.stringify(content)},`,
);

const fileBody = `${banner}

/**
 * Embedded Eta template tree. Keys are template basenames (e.g.
 * \`"docker-compose.prod.yml.eta"\`). Values are the template source
 * inlined at codegen time and bundled into the binary by
 * \`bun build --compile\` (CLI-031, same pattern as DIST-001 1a-2).
 */
export const TEMPLATES_MANIFEST: Readonly<Record<string, string>> = Object.freeze({
${entryLines.join("\n")}
});
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, fileBody, "utf-8");
console.info(`✓ Wrote ${relative(repoRoot, outFile)} (${entries.length} templates)`);
