#!/usr/bin/env node
// Codegen — DIST-001 1a-2.
// Scanne packages/arc-cli/playbooks/ et écrit un manifest TypeScript
// qui expose un Record<relPath, content> consommé par l'EmbeddedPlaybooksLoader.
//
// Output : packages/arc-cli/src/playbooks-manifest.ts (gitignored).
// Re-exécuté à chaque pnpm prebuild / pretest / pretypecheck pour
// garantir que le manifest reflète l'arborescence courante.
//
// Implémentation : strings inline JSON-stringifiées (cf. DIST-001 1a-2
// note de pivot). Équivalent fonctionnel à `import ... with { type:
// "text" }` (spike 1a-1 méthode B) — Bun inline les chaînes dans le
// binaire dans les deux cas, mais cette approche est portable
// (pas de dépendance à une syntaxe Bun-only) et n'exige aucune
// déclaration ambient .d.ts pour tsc.

import { mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");
const playbooksRoot = resolve(repoRoot, "packages/arc-cli/playbooks");
const outFile = resolve(repoRoot, "packages/arc-cli/src/playbooks-manifest.ts");

function walk(dir) {
  const out = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = `${dir}/${entry.name}`;
    if (entry.isDirectory()) out.push(...walk(full));
    else if (entry.isFile()) out.push(full);
  }
  return out;
}

const absFiles = walk(playbooksRoot).sort();
const entries = absFiles.map((abs) => {
  const rel = relative(playbooksRoot, abs).replaceAll("\\", "/");
  const content = readFileSync(abs, "utf-8");
  return { rel, content };
});

const banner = `// AUTO-GENERATED by scripts/generate-playbooks-manifest.mjs.
// DO NOT EDIT. Regenerated by pnpm prebuild / pretest / pretypecheck.
// Source : packages/arc-cli/playbooks/ (${entries.length} files).`;

const entryLines = entries.map(
  ({ rel, content }) => `  ${JSON.stringify(rel)}: ${JSON.stringify(content)},`,
);

const fileBody = `${banner}

/**
 * Embedded playbook tree. Keys are paths relative to
 * \`packages/arc-cli/playbooks/\`. Values are the file contents inlined
 * at codegen time and bundled into the binary by \`bun build --compile\`
 * (DIST-001 1a-1 spike validated this approach).
 */
export const PLAYBOOKS_MANIFEST: Readonly<Record<string, string>> = Object.freeze({
${entryLines.join("\n")}
});
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, fileBody, "utf-8");
console.info(`✓ Wrote ${relative(repoRoot, outFile)} (${entries.length} files)`);
